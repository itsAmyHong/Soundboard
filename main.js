	function chicken(){
		 document.getElementById("audio").innerHTML="<embed src='sound/chicken.mp3' hidden='true' autostart='true' loop='false' />";
		 
		 var chickenGuess;
		 
		 chickenGuess=prompt("What kind of animal am I?");
		 
		 if (chickenGuess == "Chicken" || chickenGuess == "chicken"){
			 alert("Great Job!\nYou are correct, I am a Chicken!");
		 }
		 
		 else{
		 	alert("Sorry, I am not a " + chickenGuess + ".");
		 }
	}
	
